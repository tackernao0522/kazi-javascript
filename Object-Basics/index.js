// Object Basics

let name = 'Kazi';
let age = 40;
let hobby = 'Reading';

const user = {
    name: 'Kazi',
    age: 40,
    hobby: 'Reading'
}
console.log(user); // {name: 'Kazi', age: 40, hobby: 'Reading'}
console.log(user.name); // Kazi
console.log(user['name']); // Kazi
console.log(user.age); // 40
console.log(user['age']); // 40
console.log(user.hobby) // Reading
console.log(user['hobby']); // Reading