// Arrow Functions

const calSumArrow = (number1, number2) => number1 + number2;

console.log(calSumArrow(50, 60));

// Function Expression
const mulByTwo = function(number1) {
    return number1 * 2;
}
console.log(mulByTwo(6));

// Arrow Function
const mulByTwoArr = number1 => number1 * 2;
console.log(mulByTwoArr(8))
