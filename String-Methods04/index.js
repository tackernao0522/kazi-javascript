let message = 'Hi My name Is Hi Ariyan';
console.log(message.slice(0, 5)); // Hi My
console.log(message.slice(6, 12)); // name I
console.log(message.slice(2)); // My name Is Hi Ariyan
console.log(message.slice()); // Hi My name Is Hi Ariyan
console.log(message.slice(0)); // Hi My name Is Hi Ariyan
console.log(message.slice(-12, -1)); // Is Hi Ariya

console.log(message.substring(0, 3)); // Hi
console.log(message.substr(0, 4)); // Hi M
