// Joining Array

let numbers = [1, 2, 3, 4];
let joinArray = numbers.join('-');
console.log(joinArray); // 1-2-3-4

let message = 'Hi My name is ariyan';
const arrayMessage = message.split(' ');
console.log(arrayMessage); // ['Hi', 'My', 'name', 'is', 'ariyan']
console.log(arrayMessage.join('-')); // Hi-My-name-is-ariyan
