// Switch case with multiple logical Operators
// Take a Number
// number > 0
// number === 0
// number < 0

let number = -1;

switch (true) {
    case number > 0:
        console.log('This is a Positive Number');
        break;
    case number === 0:
        console.log('This is a Zero Number');
        break;
    case number < 0:
        console.log('This is a Negative Number');
        break;
}
